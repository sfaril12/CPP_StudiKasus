#include <iostream>
using namespace std;
int main() {

    int a = 7;
    int b = 5;

    int sum = a + b;
    cout << "Hasil Penjumlahan (a + b): " << sum << endl;
    int difference = a - b;
    cout << "Hasil Pengurangan (a - b): " << difference << endl;
    int product = a * b;
    cout << "Hasil Perkalian (a * b): " << product << endl;
    float quotient =float(a) / b;
    cout << "Hasil Pembagian (a / b): " << quotient << endl;
    int remainder = a % b;
    cout << "Hasil Modulus (a % b): " << remainder << endl;
    return 0;
}


