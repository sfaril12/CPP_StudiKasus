#include <iostream>
using namespace std;
int main() {
    int i = 1;
    while (i <= 5) { // Kondisi perulangan
        cout << i << endl; // Mencetak nilai i
        i++; // Increment nilai i
    }
    cout<<endl;
    int j = 1; // Inisialisasi variabel
    do {
        cout << j << endl; // Mencetak nilai i
        j++; // Increment nilai i
    } while (j <= 5); // Kondisi perulangan

}
